package NormalProgramingQue;
   import java.util.*;
public class OddNuEven {

	public static void main(String[] args) {
		  Scanner  sc = new Scanner(System.in);
          int num = sc.nextInt();
          if(num%2==0) {
        	  System.out.println(num + " "+ "Number is even");
          }
          else {
        	  System.out.println(num + " "+ "Number is odd");
          }
	}

}
